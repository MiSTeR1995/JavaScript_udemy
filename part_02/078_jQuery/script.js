// Можно столкнуться с ошибкой $ is not defined
// связано с импортом скрипта из библиотеки. Поэтому нужно импортировать главную
// сущность из библиотеки, например функцию $, также все сущности которые
// подвязаны на эту функциональность тоже импортируются
// ради возможности получать элементы через $ не рекомендуется импортировать всю библиотеку.

// http://jquery.page2page.ru - полная документация jquery
