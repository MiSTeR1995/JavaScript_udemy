// Как всегда, при создании реальных скриптов нужно удостовериться, что наша
// DOM структура готова , что мы с ней можем работать. Использовали: DOMContentLoaded
// в jQuery это проще:

$(document).ready(function() {
    // при наведении на кнопку будем изменять класс активности
    // Необходимо сказать, что это первый элемент с помощью jQuery :first
    $('.list-item:first').hover(function() {
        // через контекст вызова обращаемся к самому элементу, на котором прошло событие
        $(this).toggleClass('active'); // переключаем класс
    });

    // когда кликаем на 3 кнопку, берем четные изображения в классе и будем их скрывать
    $('.list-item:eq(2)').click(() => { // или .on('click" () => {})
        $('.image:even').fadeToggle('slow'); // плавное скрытие элемента или показ
    });

    // прописываем анимацию вручную к пятой кнопке
    $('.list-item:eq(4)').click(() => { // или .on('click" () => {})
        $('.image:odd').animate({
            opacity: 'toggle',
            height: 'toggle'
        }, 2000); // плавное скрытие элемента или показ
    });
});


